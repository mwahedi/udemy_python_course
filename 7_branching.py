### BRANCHING IN PYTHON

## chapter is about loops
## most important is going to be the ternary operator
## operator is used when a decision is made to pass a value to a variable

passerby_speech = "Hello"
me = "Hi" if passerby_speech == "Hello" or passerby_speech == "Hi" else "Hey"
print(me)

# can also be done with numbers
a = 3
a = 7 if 3**2 > 9 else 14
print(a)

## ternary operators are used when deciding what value needs to be passed to a variable